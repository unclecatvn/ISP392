    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        String questionId = request.getParameter("questionId");
        String classId = request.getParameter("classId");
        String courseId = request.getParameter("courseId");
        String quizId = request.getParameter("quizId");
        AnswerDAO answerDAO = new AnswerDAO();
        answerDAO.deleteAllAnswerByQuestionId(Integer.parseInt(questionId));
        QuestionDAO questionDAO = new QuestionDAO();
        questionDAO.deleteQuestionByQuestionId(Integer.parseInt(questionId));
        questionDAO.updateTotalQuestion(Integer.parseInt(quizId),"-");
        response.sendRedirect("editQuiz?classId=" + Integer.parseInt(classId) + "&courseId=" + Integer.parseInt(courseId) + "&quizId=" + quizId + "&in=Delete+successfully");
    }

